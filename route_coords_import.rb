def routeWithPoints (routeName, routeID, coords)
  r = Route.new(:name => routeName)
  r.id = routeID
  points = []
  coords.each_with_index { |item, index|
    p = RoutePoint.new(:route_order => index, :latitude => item[0], :longitude => item[1])
    points.push(p)
  }

  r.route_points = points
  r.save
end

